def load_jsonl(path: Path) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    with path.open("r", encoding="utf-8") as handle:
        for raw_line in handle:
            line = raw_line.strip()
            if not line:
                continue
            rows.append(json.loads(line))
    return rows


def write_jsonl(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        for row in rows:
            handle.write(json.dumps(row, ensure_ascii=False) + "\n")


def append_cases(file_name: str, new_cases: list[dict[str, Any]]) -> tuple[int, int]:
    path = EVAL_DIR / file_name
    rows = load_jsonl(path)
    existing_ids = {str(row.get("eval_id")) for row in rows}
    additions = [case for case in new_cases if case["eval_id"] not in existing_ids]
    if additions:
        rows.extend(additions)
        write_jsonl(path, rows)
    return len(additions), len(rows)


